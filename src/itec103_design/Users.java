    private void getAllUsers(String query){
            
            List<User> users = hp.getUsers(query);
            DefaultTableModel model = (DefaultTableModel) userTable.getModel();
            TableColumnModel columnModel = userTable.getColumnModel();
            userTable.setRowHeight(30);
            columnModel.getColumn(0).setMinWidth(0);        
            columnModel.getColumn(0).setMaxWidth(0);
            columnModel.getColumn(1).setMinWidth(30);        
            columnModel.getColumn(1).setMaxWidth(30);
            int count = 0;
            model.setNumRows(0);
            for (User user : users) {
                count += 1;
                String role = (Integer.parseInt(user.getRole()) == 1) ? "Admin" : "Employee";
                model.addRow(new Object[]{user.getUserId(),count, user.getFirstname(), user.getLastname(), role, user.getCreatedAt()});
            }
    }

