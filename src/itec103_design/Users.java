    private boolean matchedWordLike(String word, String searchWord) {
       boolean found = word.toLowerCase().contains(searchWord.toLowerCase());
       return found;
    }
    
    private void searchUser() {
        String srchValue = search_user.getText();
        String query = null;
        String role = null;
        boolean admin = matchedWordLike("Admin", srchValue);        
        boolean emp = matchedWordLike("Employee", srchValue);
        if(admin) role = "1";
        if(emp) role = "0";
        
        if(!search_user.getText().isBlank()){
            query = "SELECT * FROM users where firstname LIKE '%"+srchValue+"%' OR lastname LIKE '%"+srchValue+"%' OR role LIKE '%"+role+"%' OR created_at LIKE '%"+srchValue+"%'";
            getAllUsers(query);
        } else {
            getAllUsers("SELECT * FROM users ORDER BY user_id ASC");
        }    
    }
